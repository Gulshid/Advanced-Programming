# string ==> len, upper, lower, capitalize
name = 'python'
print("Len:", len(name))
print('upper :', name.upper())
print("Lower :", name.lower())
print("Capitalize: ", name.capitalize())


# indexing and slicing  => index 0, index last , index slice
text = 'python'
print("index 0 :", text[0])
print("last index :", text[-1])
print("SLicing :", text[2 : 5])

# string method ==> startwith, endwuth, find, replace
sent = "I love python programming"
print("Startwith:", sent.startswith("I"))
print("Endwith: ", sent.endswith("ing"))
print("find python :", sent.find("python"))
print("Replace :", sent.replace("love", "ENjoy"))


